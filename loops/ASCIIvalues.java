package loops;

public class ASCIIvalues {
    public static void main(String[] args) {

        //printing ascii for capital alpahbet
        for(char a = 'A'; a<= 'Z'; a++) {
            int i = a;
            System.out.println(a + " = " + i);
        }
        
        //printing ascii for small alpahbet
        for(char a = 'a'; a<= 'z'; a++) {
            int i = a;
            System.out.println(a + " = " + i);
        }
    }
}
